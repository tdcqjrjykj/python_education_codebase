"""
概念：
变量是存储数据值的容器，它有一个名字（标识符）和一个值。在Python中，变量不需要事先声明类型，其类型在赋值时自动确定。
赋值：
使用赋值运算符=将值赋给变量。
"""
"""
数据类型：整数、浮点数、字符串
整数：不带小数点的数。
浮点数：带有小数点的数。
字符串：由字符组成的序列，用引号（单引号或双引号）包围。
"""

# 声明并赋值一个整数变量
int_variable = 10
print("整数变量:", int_variable)

# 声明并赋值一个浮点数变量
float_variable = 3.14
print("浮点数变量:", float_variable)

# 声明并赋值一个字符串变量
str_variable = "Hello, World!"
print("字符串变量:", str_variable)
