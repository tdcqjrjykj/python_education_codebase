# 创建一个字典
my_dict = {'name': 'Alice', 'age': 25, 'city': 'New York'}

# 访问字典中的值
print(my_dict['name'])  # 输出: Alice

# 修改字典中的值
my_dict['age'] = 30
print(my_dict['age'])  # 输出: 30

# 添加键值对到字典
my_dict['country'] = 'USA'
print(my_dict)  # 输出: {'name': 'Alice', 'age': 30, 'city': 'New York', 'country': 'USA'}